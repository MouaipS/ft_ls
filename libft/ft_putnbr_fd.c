#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		a;
	char	b;

	a = 1;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		a = -1;
	}
	if (n / 10)
		ft_putnbr_fd(n / 10 * a, fd);
	b = (n % 10) * a + 48;
	ft_putchar_fd(b, fd);
}
