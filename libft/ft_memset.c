#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	int		i;
	char	*s2;

	s2 = s;
	i = 0;
	if (n == 0)
		return (s);
	while (n != 0)
	{
		s2[i] = (unsigned char)c;
		i++;
		n--;
	}
	return (s);
}
